    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        try{
            Checker check = new Checker();
          
            if(newPasswordField.getText().isEmpty() || confirmPasswordField.getText().isEmpty()){
                JOptionPane.showMessageDialog((Component)null, "Please enter credentials!");
            }  
            else if(!check.isPassword(newPasswordField.getText()) || !check.isPassword(confirmPasswordField.getText())){
                JOptionPane.showMessageDialog((Component)null, "Length of password should be atleast 7 characters with atleast 1 digit and alphabetic character!");
            }
            else if(!newPasswordField.getText().equals(confirmPasswordField.getText())){
                JOptionPane.showMessageDialog((Component)null, "Passwords do not match!");
            }
            else{
                int result = JOptionPane.showConfirmDialog(null,"Are you sure you want to change your password?", "Password Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
                if(result == JOptionPane.YES_OPTION){
                    Statement stmt = check.connectSQL();
                    String query = "Update Person set password='"+newPasswordField.getText()+"' where user_ID ='"+uID+"'";
                    stmt.executeUpdate(query);
                    
                    dispose();
                    Login login = new Login();
                    login.setVisible(true);                //frame will only be visible with set "true"
                    login.setLocationRelativeTo(null);     //for opening in center
                } 
                
            }
        }catch(Exception ex){
            System.out.println(ex.toString());
        }
        
        
    }//GEN-LAST:event_submitBtnActionPerformed


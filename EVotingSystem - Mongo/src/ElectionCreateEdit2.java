    private void addVoterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addVoterBtnActionPerformed
        try{
            int result = JOptionPane.showConfirmDialog(null,"Are you sure you want to add the voter?", "Voter Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if(result == JOptionPane.YES_OPTION){
                //voter id generated
                IDGenerator idgen = new IDGenerator();
                String vid = idgen.generateVoterID(voterIDs);
                
                //voter data added
                ArrayList<String> voterData = new ArrayList<>();
                voterData.add("voter");
                voterData.add(uIDField.getText());
                voterData.add(vid);
                
                //checks if voter details already added to data arrayList or not
                boolean repeats = false;
                for(int i=0; i<data.size(); i++){
                    if(data.get(i).get(1).equalsIgnoreCase(uIDField.getText()) && data.get(i).get(0).equalsIgnoreCase("voter")){
                        repeats = true;
                        break;
                    }
                }
                
                if(!repeats){
                    voterIDs.add(vid);
                    this.data.add(voterData);
                }
                else{
                    JOptionPane.showMessageDialog((Component)null, "Voter already added!");
                }
                
                System.out.println(data);
                uIDField.setText("");
                cnicField.setText("xxxxx-xxxxxxx-x");
                fnameField.setText("");
                lnameField.setText("");
                emailField.setText("");
                ageField.setText("");
                phoneField.setText("");
                genderGroup.clearSelection();
                addVoterBtn.setEnabled(false);
            } 
            
       
        }catch(Exception ex){
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_addVoterBtnActionPerformed


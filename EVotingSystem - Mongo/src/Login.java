    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        try{
            Checker check = new Checker();
            
            if(emailField.getText().isEmpty() || passwordField.getText().isEmpty()){
                JOptionPane.showMessageDialog((Component)null, "Please enter credentials!");
            }  
            else{
                String uid = check.findLoginCredentials(emailField.getText(), passwordField.getText());
                if (uid == null){
                    JOptionPane.showMessageDialog((Component)null, "Invalid Email/Password!");
                }
                else{
                    JOptionPane.showMessageDialog((Component)null, "Login Successful!");
                    dispose();
                    MainScreen main = new MainScreen(uid);
                    main.setVisible(true);                //frame will only be visible with set "true"
                    main.setLocationRelativeTo(null);     //for opening in center
                } 
            }      
        }catch(Exception ex){
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_loginBtnActionPerformed

